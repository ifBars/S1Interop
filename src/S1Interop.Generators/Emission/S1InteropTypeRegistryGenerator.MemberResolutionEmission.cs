        builder.AppendLine("                case S1InteropMemberKind.Property:");
        builder.AppendLine("                    return ResolveProperty(ownerType, ownerTypeName, memberName, kind);");
        builder.AppendLine("                default:");
        builder.AppendLine("                    return ResolveProperty(ownerType, ownerTypeName, memberName, kind) ?? (System.Reflection.MemberInfo?)ResolveField(ownerType, ownerTypeName, memberName, kind);");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
