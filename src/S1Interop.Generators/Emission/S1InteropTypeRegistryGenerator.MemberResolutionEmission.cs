        builder.AppendLine();
        builder.AppendLine("                return field;");
        builder.AppendLine("            }");
        builder.AppendLine("            catch (System.Exception exception)");
        builder.AppendLine("            {");
        builder.AppendLine("                Record(S1InteropMemberResolutionStatus.ResolutionFailed, ownerTypeName, memberName, kind, parameterTypeNames: null, \"Field resolution failed for the active backend.\", exception);");
        builder.AppendLine("                return null;");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        private static System.Reflection.PropertyInfo? ResolveProperty(System.Type ownerType, string ownerTypeName, string memberName, S1InteropMemberKind kind)");
        builder.AppendLine("        {");
        builder.AppendLine("            try");
        builder.AppendLine("            {");
        builder.AppendLine("                System.Reflection.PropertyInfo? property = ownerType.GetProperty(memberName, AllBindings);");
