        builder.AppendLine("        private static System.Reflection.FieldInfo? ResolveField(System.Type ownerType, string ownerTypeName, string memberName, S1InteropMemberKind kind)");
        builder.AppendLine("        {");
        builder.AppendLine("            try");
        builder.AppendLine("            {");
        builder.AppendLine("                System.Reflection.FieldInfo? field = ownerType.GetField(memberName, AllBindings);");
